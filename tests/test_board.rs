use tp1taller::translate::translate;

#[warn(unused_imports)]
#[test]
fn test_board1() {
    let sl = [
        46, 42, 46, 42, 46, 10, // - * - * -
        46, 46, 42, 46, 46, 10, // - - * - -
        46, 46, 42, 46, 46, 10, // - - * - -
        46, 46, 46, 46, 46,     // - - - - -
    ];
    assert_eq!(
        translate(&sl),
        Vec::from([
            49, 42, 51, 42, 49, 10, // 1 * 3 * 1
            49, 51, 42, 51, 49, 10, // 1 3 * 3 1
            46, 50, 42, 50, 46, 10, // - 2 * 2 -
            46, 49, 49, 49, 46, 10  // - 1 1 1 -
        ])
    );
}

#[test]
fn test_board2() {
    let sl = [
        46, 42, 42, 42, 46, 10, // - * * * -
        42, 46, 46, 42, 46, 10, // * - - * -
        46, 46, 46, 46, 46, 10, // - - - - -
        46, 46, 46, 46, 46,     // - - - - -
    ];
    assert_eq!(
        translate(&sl),
        Vec::from([
            50, 42, 42, 42, 50, 10, // 2 * * * 2
            42, 51, 52, 42, 50, 10, // * 3 4 * 2
            49, 49, 49, 49, 49, 10, // 1 1 1 1 1
            46, 46, 46, 46, 46, 10  // - - - - -
        ])
    );
}


#[test]
fn test_board3() {
    let sl = [
        46, 46, 46, 46, 46, 46, 46, 46, 10, // - - - - - - - - 
        46, 46, 46, 46, 46, 42, 42, 42, 10, // - - - - - * * *
        46, 46, 46, 46, 42, 46, 46, 46, 10, // - - - - * - - -
        46, 42, 46, 46, 46, 46, 46, 46, 10, // - * - - - - - -
        46, 46, 46, 46, 46, 46, 46, 42      // - - - - - - - *
    ];
    assert_eq!(
        translate(&sl),
        Vec::from([
            46, 46, 46, 46, 49, 50, 51, 50, 10, // - - - - 1 2 3 2 
            46, 46, 46, 49, 50, 42, 42, 42, 10, // - - - 1 2 * * *
            49, 49, 49, 49, 42, 51, 51, 50, 10, // 1 1 1 1 * 3 3 2
            49, 42, 49, 49, 49, 49, 49, 49, 10, // 1 * 1 1 1 1 1 1
            49, 49, 49, 46, 46, 46, 49, 42, 10     // 1 1 1 - - - 1 *
        ])
    );
}
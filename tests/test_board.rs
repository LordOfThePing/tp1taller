use tp1taller::translate::translate;



#[warn(unused_imports)]
#[test]
fn test_board() {
    assert_eq!(
        translate(&[
            46, 42, 46, 42, 46, 10, // 1 * s3 * 1
            46, 46, 42, 46, 46, 10, // 1 3 * 3 1
            46, 46, 42, 46, 46, 10, // - 2 * 2 -
            46, 46, 46, 46, 46      // - 1 1 1 -
        ]),
        Vec::from([
            49, 42, 51, 42, 49, 10, 
            49, 51, 42, 51, 49, 10, 
            46, 50, 42, 50, 46, 10, 
            46, 49, 49, 49, 46
        ])
    );
}
